            if(chkDados.isSelected()){
                //gerar.gerar("from ClientePf where id =" +idCliente, "src/relatorios/relClientePf.jasper");
            }
            if(chkHonorarios.isSelected()){
                //gerar.gerarRelatorios("from ClientePf where id =" +idCliente, "ARQUIVO");
            }
            if(chkPobreza.isSelected()){
                gerar.gerarDeclaracaoPobreza(cliente, aMaster);
            }
