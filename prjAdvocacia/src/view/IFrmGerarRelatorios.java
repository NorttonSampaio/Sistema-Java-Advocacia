    public void carregarcbxCliente(){
        String sql="";
        if(rbPFisica.isSelected() && !rbPJuridica.isSelected()){
            sql="from ClientePf where ativo = '1' order by nome";
            Session s=null;
            try{
                s = DAOSistema.getSessionFactory().openSession();
                s.beginTransaction();

                Query q = s.createQuery(sql);
                List l = q.list();
                Iterator i = l.iterator();
                
                vetorCliente = new int[l.size()];

                ClientePf c;
                cbxCliente.removeAllItems();
                int cont=0;

                while(i.hasNext()){
                    c=(ClientePf)i.next();
                    cbxCliente.addItem(c.getNome());
                    vetorCliente[cont]=c.getId();
                    cont++;
                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(IFrmGerarRelatorios.this, ex);
                ex.printStackTrace();
            }finally{
                s.getTransaction().commit();
                s.close();
            }
            AutoCompleteDecorator.decorate(cbxCliente);
        }else{
            sql="from ClientePj where ativo = '1' order by nomeFantasia";
            Session s = null;
            try{
                s = DAOSistema.getSessionFactory().openSession();
                s.beginTransaction();

                Query q = s.createQuery(sql);
                List l = q.list();
                Iterator i = l.iterator();

                vetorCliente = new int[l.size()];
                
                ClientePj c;
                cbxCliente.removeAllItems();
                int cont=0;

                while(i.hasNext()){
                    c=(ClientePj)i.next();
                    cbxCliente.addItem(c.getNomeFantasia());
                    vetorCliente[cont]=c.getId();
                    cont++;
                }
                
            }catch(Exception ex){
                JOptionPane.showMessageDialog(IFrmGerarRelatorios.this, ex);
                ex.printStackTrace();
            }finally{
                s.getTransaction().commit();
                s.close();
            }
            AutoCompleteDecorator.decorate(cbxCliente);
        }
    }
    
